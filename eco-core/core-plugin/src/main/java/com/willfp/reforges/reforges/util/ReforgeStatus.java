package com.willfp.reforges.reforges.util;

public enum ReforgeStatus {
    /**
     * Allow the reforge.
     */
    ALLOW,

    /**
     * Allow the reforge with a stone.
     */
    ALLOW_STONE,

    /**
     * Invalid item to reforge.
     */
    INVALID_ITEM,

    /**
     * No item at all.
     */
    NO_ITEM
}
